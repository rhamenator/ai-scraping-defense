#!/bin/sh
set -eu

OUTPUT_PATH="${1:-/etc/nginx/generated-conf/20-cdn-origin-lockdown.conf}"
LOCKDOWN_ENABLED="${SECURITY_CDN_ORIGIN_LOCKDOWN:-false}"
TRUSTED_CIDRS="${SECURITY_CDN_TRUSTED_PROXY_CIDRS:-}"

is_truthy() {
  case "$(printf '%s' "${1:-}" | tr '[:upper:]' '[:lower:]')" in
    1|true|yes|on) return 0 ;;
    *) return 1 ;;
  esac
}

trim_whitespace() {
  value=${1:-}
  value=${value#"${value%%[![:space:]]*}"}
  value=${value%"${value##*[![:space:]]}"}
  printf '%s' "$value"
}

normalize_cidr() {
  value=$(trim_whitespace "${1:-}")

  if [ -z "$value" ]; then
    return 1
  fi

  case "$value" in
    *[!0-9A-Fa-f:./]*|*/*/*)
      return 1
      ;;
  esac

  prefix=${value#*/}
  if [ "$prefix" != "$value" ]; then
    address=${value%/*}
    case "$prefix" in
      ''|*[!0-9]*)
        return 1
        ;;
    esac
    if [ "$prefix" -gt 128 ]; then
      return 1
    fi
  else
    address=$value
  fi

  if [ -z "$address" ]; then
    return 1
  fi

  printf '%s' "$value"
}

write_disabled_file() {
  cat >"$OUTPUT_PATH" <<'EOF'
# Generated by render_cdn_origin_lockdown.sh
# CDN origin lockdown is disabled for this runtime.
EOF
}

if ! is_truthy "$LOCKDOWN_ENABLED"; then
  write_disabled_file
  exit 0
fi

{
  echo "# Generated by render_cdn_origin_lockdown.sh"
  echo "# Allow local container health checks even when CDN origin lockdown is enabled."
  echo "allow 127.0.0.1/32;"
  echo "allow ::1/128;"
  OLD_IFS=${IFS}
  IFS=','
  for cidr in $TRUSTED_CIDRS; do
    trimmed=$(normalize_cidr "$cidr") || {
      printf >&2 'Invalid SECURITY_CDN_TRUSTED_PROXY_CIDRS entry: %s\n' "$cidr"
      exit 1
    }
    if [ -n "$trimmed" ]; then
      echo "allow $trimmed;"
    fi
  done
  IFS=${OLD_IFS}
  echo "deny all;"
} >"$OUTPUT_PATH"
